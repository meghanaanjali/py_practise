def sum (num1,num2):
    return num1 + num2

def sub (num1,num2):
    return num1 - num2

def mult (num1,num2):
    return num1 * num2

def div (num1,num2):
    return num1 / num2

num1 = 5
num2 = 4
print('sum = ', sum(num1,num2))
print('sub = ', sub(num1,num2))
print('mult = ', mult(num1,num2))
print('div = ', div(num1,num2))
